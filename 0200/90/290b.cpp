void answer(unsigned x)
{
    std::cout << x << '\n';
}

void solve(unsigned x, unsigned y)
{
    answer(image[x][y] == ' ');
}

int main()
{
    std::cin.tie(nullptr)->sync_with_stdio(false);

    unsigned x, y;
    std::cin >> x >> y;

    solve(x, y);

    return 0;
}
